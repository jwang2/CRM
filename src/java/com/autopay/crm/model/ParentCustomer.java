package com.autopay.crm.model;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Judy
 */
@Entity
@Table(name = "parent_customer")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "ParentCustomer.findAll", query = "SELECT p FROM ParentCustomer p"),
    @NamedQuery(name = "ParentCustomer.findById", query = "SELECT p FROM ParentCustomer p WHERE p.id = :id"),
    @NamedQuery(name = "ParentCustomer.findByName", query = "SELECT p FROM ParentCustomer p WHERE p.name = :name"),
    @NamedQuery(name = "ParentCustomer.findByCompareName", query = "SELECT p FROM ParentCustomer p WHERE p.compareName = :compareName")})
public class ParentCustomer implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Long id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 255)
    @Column(name = "name")
    private String name;
    @Size(max = 255)
    @Column(name = "compare_name")
    private String compareName;
    @OneToMany(mappedBy = "parentCustomerId")
    private Collection<Customer> customerCollection;

    public ParentCustomer() {
    }

    public ParentCustomer(Long id) {
        this.id = id;
    }

    public ParentCustomer(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCompareName() {
        return compareName;
    }

    public void setCompareName(String compareName) {
        this.compareName = compareName;
    }

    @XmlTransient
    public Collection<Customer> getCustomerCollection() {
        return customerCollection;
    }

    public void setCustomerCollection(Collection<Customer> customerCollection) {
        this.customerCollection = customerCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ParentCustomer)) {
            return false;
        }
        ParentCustomer other = (ParentCustomer) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.autopay.crm.model.ParentCustomer[ id=" + id + " ]";
    }
    
}
